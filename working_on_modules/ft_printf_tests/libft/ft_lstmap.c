#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_list;
	t_list	*new_obj;
	void	*tmp_content;

	new_list = NULL;
	while (lst != NULL)
	{
		tmp_content = f(lst->content);
		new_obj = ft_lstnew(tmp_content);
		if (new_obj == NULL)
		{
			ft_lstclear(&new_list, del);
			del(tmp_content);
			return (NULL);
		}
		ft_lstadd_back(&new_list, new_obj);
		lst = lst->next;
	}
	return (new_list);
}
