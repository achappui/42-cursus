#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	len;

	len = 0;
	while (*s++ != '\0')
		len++;
	s -= len + 1;
	write(fd, s, len);
	write(fd, "\n", 1);
}
